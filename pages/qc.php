                    <h3>［ 労働安全衛生への取り組み ］</h3>
                    <p>OHSAS18001によるマネジメントプログラムを中心に、各部署で危険箇所の洗い出しや改善を定期的に行なっており、教育も徹底し従業員が怪我をしないような作業環境つくりや意識付けを行っております。また、労働安全衛生や交通安全のスローガンを朝礼で唱和して従業員全員で意識を高めております。他にもメンタルヘルスや熱中症対策などの勉強会も開催しています。<br>
                        ミルボンの工場では、労働安全衛生を徹底することで、「従業員が健康かつ働きやすい職場環境」につながると考えおります。</p>
                    <h3>［ OHSAS18001認証 ］</h3>
                    <p>オーサス18001とは、ISO14001（環境）の労働安全衛生版と いえます。Ｐ・Ｄ・Ｃ・Ａ（計画—実施—評価—改善）のシステ ムを労働安全衛生活動の基本として、“潜在的リスク”の発生 を予防し、安全衛生を確保していく規格のことです。<br>
                        ゆめが丘工場では2006年10月、青山工場では2007年10月にJCQA審査会社によって認証を得て、現在に至っています。</p>
                    <h3>［ 障がい者雇用の支援 ］</h3>
                    <p>ゆめが丘工場は、全面的なバリアフリーの実現によって、障がい者の方々が社員として安心して仕事に励んでいただける、あらゆる人にやさしい事業拠点となっています。また、知的障がいを抱える方々へのグループワークによる業務の依頼は、2010年度外注実績で約1900万円に及んでおり、その真剣に仕事に取り組んでいただく姿は、テレビでも紹介されています。<br>
                        今後も、意欲的に仕事に取り組んでもらえる障がい者の方々とは、直接または間接的な雇用関係を結んでいきたいと考えています。</p>
